# Converting BP to 2 type columns Systolic and Diastolic
def parse_bp(bp):
    try:
        if pd.isna(bp): return (np.nan, np.nan)
        p = str(bp).split('/')
        return int(p[0]), int(p[1])
    except:
        return (np.nan, np.nan)

