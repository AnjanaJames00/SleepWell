def search_pubmed(query, max_results=3):
    """Fetch PubMed article titles safely."""
    try:
        base = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/esearch.fcgi"
        params = {"db": "pubmed", "term": query, "retmode": "json", "retmax": max_results}
        resp = requests.get(base, params=params, timeout=10)
        resp.raise_for_status()
        data = resp.json()
        ids = data.get("esearchresult", {}).get("idlist", [])
        if not ids:
            return None

        summaries = []
        for pid in ids:
            s_url = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/esummary.fcgi"
            s = requests.get(s_url, params={"db": "pubmed", "id": pid, "retmode": "json"}, timeout=10)
            s.raise_for_status()
            info = s.json()["result"].get(pid, {})
            title = info.get("title", "No Title")
            date = info.get("pubdate", "n.d.")
            summaries.append(f"🧾 [{title}](https://pubmed.ncbi.nlm.nih.gov/{pid}/) ({date})")
        return "\n".join(summaries)
    except Exception as e:
        print("⚠ PubMed error:", e)
        return None


def search_europepmc(query, max_results=3):
    """Fetch EuropePMC results safely."""
    try:
        url = f"https://www.ebi.ac.uk/europepmc/webservices/rest/search"
        params = {"query": query, "format": "json", "pageSize": max_results}
        r = requests.get(url, params=params, timeout=10)
        r.raise_for_status()
        data = r.json()
        results = data.get("resultList", {}).get("result", [])
        if not results:
            return None

        out = []
        for item in results:
            title = item.get("title", "No Title")
            src = item.get("source", "MED")
            pid = item.get("id", "N/A")
            link = f"https://europepmc.org/article/{src}/{pid}"
            year = item.get("pubYear", "n.d.")
            out.append(f"🧾 [{title}]({link}) ({year})")
        return "\n".join(out)
    except Exception as e:
        print("⚠ EuropePMC error:", e)
        return None


def medical_chatbot(user_query, patient_features=None, shap_text=None):
    """Generate structured medical response with live or fallback evidence."""
    # ✅ Try cache first
    if user_query in evidence_cache:
        evidence_text = evidence_cache[user_query]
    else:
        pubmed = search_pubmed(user_query)
        epmc = search_europepmc(user_query)

        if pubmed or epmc:
            evidence_text = f"""
🔎 *PubMed Evidence*
{pubmed or 'No PubMed results.'}

🌍 *EuropePMC Evidence*
{epmc or 'No EuropePMC results.'}
"""
        else:
            evidence_text = LOCAL_FALLBACK

        evidence_cache[user_query] = evidence_text

    # ✅ Build LLM prompt
    prompt = f"""
You are a medically-safe assistant focusing on sleep disorders.
Be evidence-based, clear, and concise.
Never diagnose, only explain patterns and offer general advice.

USER QUESTION:
{user_query}

PATIENT DETAILS:
{patient_features}

MODEL EXPLANATION (SHAP):
{shap_text}

EVIDENCE:
{evidence_text}

RESPONSE FORMAT:
Assessment:
Medical Explanation:
Evidence Summary:
Personalized Sleep Hygiene Plan:
Red-flag Warning (if any):
"""

    try:
        res = llm(prompt, max_tokens=700, temperature=0.2)
        text = res["choices"][0]["text"] if isinstance(res, dict) else str(res)
    except Exception as e:
        text = f"⚠ LLM error: {e}\n\nUsing fallback summary:\n{LOCAL_FALLBACK}"

    return text.strip(), evidence_text

"""Prepare Input Function

"""

def prepare_input_row(user_inputs: dict):
    df_user = pd.DataFrame([user_inputs])

    # Auto-handle Gender if provided as plain text
    if "Gender" in df_user.columns:
        df_user["Gender_Male"] = (df_user["Gender"].astype(str).str.lower() == "male").astype(int)
        df_user.drop("Gender", axis=1, inplace=True)

    # Fill dummy categorical columns if missing
    for col in feature_template.columns:
        if col not in df_user.columns:
            df_user[col] = 0

    # Reindex and ensure numeric
    df_aligned = df_user.reindex(columns=feature_template.columns, fill_value=0)
    df_aligned = df_aligned.apply(pd.to_numeric, errors='coerce').fillna(0)

    # Debug print to check what goes to model
    nonzero = df_aligned.loc[:, (df_aligned != 0).any(axis=0)]
    print("🧩 Nonzero input features:\n", nonzero.T)
    return df_aligned

