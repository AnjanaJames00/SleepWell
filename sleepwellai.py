""" SHAP Function"""

def compute_local_shap(explainer_obj, sample_df, top_k=5):
    aligned = sample_df.reindex(columns=feature_cols, fill_value=0)
    X = aligned.values
    shap_vals = explainer_obj.shap_values(X)
    pred_idx = int(model.predict(X)[0])
    class_shap = np.ravel(shap_vals[pred_idx][0])
    sorted_idx = np.argsort(np.abs(class_shap))[::-1][:top_k]
    contributions = [
        f"{feature_cols[i]}** → {'+' if class_shap[i]>0 else ''}{class_shap[i]:.3f}"
        for i in sorted_idx
    ]
    return "\n".join(contributions), shap_vals[pred_idx][0]

